package xl.test.algorithm.utils;

/**
 * 一对整数
 * created by XUAN on 2019/10/18
 */
public class PairInt {

    private int first;

    private int second;

    public int getFirst() {
        return first;
    }

    public void setFirst(int first) {
        this.first = first;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }

    public PairInt() {
    }

    public PairInt(int first, int second) {
        this.first = first;
        this.second = second;
    }
}
